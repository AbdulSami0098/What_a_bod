const generateAttendance = () => {
  const records = [];
  const today = new Date('2026-05-15');
  MEMBERS.forEach((member) => {
    for (let i = 89; i >= 0; i--) {
      const date = new Date(today);
      date.setDate(today.getDate() - i);
      const dayOfWeek = date.getDay();
      if (dayOfWeek === 0 || dayOfWeek === 6) continue;
      const rand = Math.random();
      if (rand < 0.72) {
        records.push({
          id: `att-${member.id}-${i}`,
          memberId: member.id,
          date: date.toISOString().split('T')[0],
          status: rand < 0.65 ? 'present' : 'late',
          checkIn: rand < 0.65 ? `${6 + Math.floor(Math.random() * 4)}:${String(Math.floor(Math.random() * 60)).padStart(2, '0')} AM` : `${10 + Math.floor(Math.random() * 2)}:${String(Math.floor(Math.random() * 60)).padStart(2, '0')} AM`,
          checkOut: `${1 + Math.floor(Math.random() * 4)}:${String(Math.floor(Math.random() * 60)).padStart(2, '0')} PM`,
        });
      } else if (rand < 0.75) {
        records.push({
          id: `att-${member.id}-${i}`,
          memberId: member.id,
          date: date.toISOString().split('T')[0],
          status: 'absent',
          checkIn: null,
          checkOut: null,
        });
      }
    }
  });
  return records;
};

